package io.battlearena.leekhub.model.exception.webscript;

public class WebScriptException extends Exception {

	/**
	 * Serial
	 */
	private static final long serialVersionUID = -5611850291432015159L;

	public WebScriptException() {
		super();
	}

	public WebScriptException(String message, Throwable cause) {
		super(message, cause);
	}

	public WebScriptException(String message) {
		super(message);
	}

	public WebScriptException(Throwable cause) {
		super(cause);
	}
	

}
